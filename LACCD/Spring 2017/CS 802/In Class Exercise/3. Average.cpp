#include <iostream>
using namespace std;

int main()
{
	int num1 = 12;
	int num2 = 30;
	int num3 = 45;
	int ave = (num1 + num2 + num3) / 3;

	cout << "The first number is " << num1 << "." << endl;
	cout << "The second number is " << num2 << "." << endl;
	cout << "The third number is " << num3 << "." << endl;
	cout << "The average of these three numbers is " << ave << "." << endl;
	
	return 0;
}